#!/bin/bash

# Flutter Version Management (FVM) aliases
alias flutter="fvm flutter"
alias dart="fvm dart"

# Optional: Add some helpful FVM shortcuts
alias fvm-list="fvm list"
alias fvm-install="fvm install"
alias fvm-use="fvm use"

echo "FVM aliases loaded! You can now use:"
echo "  flutter doctor"
echo "  dart --version"
echo "  fvm-list (to see installed versions)"